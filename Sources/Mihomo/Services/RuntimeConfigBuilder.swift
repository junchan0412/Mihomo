import Foundation
import Yams

struct RuntimeConfigBuilder {
    private typealias YAMLMap = [String: Any]

    func build(
        profileContent: String,
        settings: AppSettings,
        fragments: [ConfigFragment] = [],
        disabledRules: Set<String> = [],
        nodeProviders: [NodeProvider] = []
    ) throws -> String {
        var configuredMap = try yamlMapping(from: profileContent, label: "profile")

        if settings.yamlOverrideEnabled {
            for fragment in fragments where fragment.enabled && fragment.kind == .yaml {
                let fragmentMap = try yamlMapping(from: fragment.content, label: fragment.name)
                configuredMap = deepMerge(configuredMap, fragmentMap)
            }
        }

        try injectNodeProviders(nodeProviders, into: &configuredMap)

        filterDisabledRules(in: &configuredMap, disabledRules: disabledRules)

        var finalMap = deepMerge(generatedOverlay(settings: settings), configuredMap)
        enforceAppManagedControlChannel(in: &finalMap, settings: settings)
        enforceNetworkRuntimePlan(in: &finalMap, settings: settings)

        let dumped = try YAMLText.dump(finalMap)
            .trimmingCharacters(in: .whitespacesAndNewlines)
        return "# Generated by Mihomo. The active Profile overrides App defaults; App edits are synchronized back to that Profile.\n\(dumped)\n"
    }

    private func generatedOverlay(settings: AppSettings) -> YAMLMap {
        let networkPlan = NetworkRuntimePlan(settings: settings)
        var overlay: YAMLMap = [
            "mixed-port": settings.mixedPort,
            "allow-lan": settings.allowLAN,
            "mode": "rule",
            "log-level": settings.logLevel,
            "external-controller": "\(controllerBindHost(settings)):\(settings.controllerPort)"
        ]

        if settings.socksPort > 0 {
            overlay["socks-port"] = settings.socksPort
        }

        if settings.controllerSecret.trimmingCharacters(in: .whitespacesAndNewlines).isEmpty == false {
            overlay["secret"] = settings.controllerSecret
        }

        if networkPlan.dnsEnabled {
            overlay["dns"] = dnsMap(settings, plan: networkPlan)
            overlay["profile"] = ["store-selected": true, "store-fake-ip": networkPlan.usesFakeIP]
        }

        if settings.snifferEnabled {
            overlay["sniffer"] = snifferMap(settings)
        }

        if networkPlan.tunEnabled {
            overlay["tun"] = [
                "enable": true,
                "stack": "mixed",
                "auto-route": true,
                "auto-detect-interface": true,
                "dns-hijack": networkPlan.dnsHijackTargets
            ]
        }

        return overlay
    }

    private func controllerBindHost(_ settings: AppSettings) -> String {
        settings.remoteAPIEnabled ? settings.remoteAPIBindAddress : "127.0.0.1"
    }

    private func snifferMap(_ settings: AppSettings) -> YAMLMap {
        let httpPorts = portList(settings.snifferHTTPPorts, fallback: [80, 443])
        let tlsPorts = portList(settings.snifferTLSPorts, fallback: [443])
        let quicPorts = portList(settings.snifferQUICPorts, fallback: [])
        var sniff: YAMLMap = [
            "HTTP": [
                "ports": httpPorts,
                "override-destination": settings.snifferOverrideDestination
            ],
            "TLS": ["ports": tlsPorts]
        ]
        if quicPorts.isEmpty == false {
            sniff["QUIC"] = ["ports": quicPorts]
        }

        var map: YAMLMap = [
            "enable": true,
            "parse-pure-ip": settings.snifferParsePureIP,
            "force-dns-mapping": settings.snifferForceDNSMapping,
            "override-destination": settings.snifferOverrideDestination,
            "sniff": sniff
        ]

        let forceDomains = lineList(settings.snifferForceDomains)
        let skipDomains = lineList(settings.snifferSkipDomains)
        let skipDestinationAddresses = lineList(settings.snifferSkipDestinationAddresses)
        let effectiveSkipDestinationAddresses = skipDestinationAddresses.isEmpty ? Self.sparkleCompatibleSnifferSkipDestinationAddresses : skipDestinationAddresses
        let skipSourceAddresses = lineList(settings.snifferSkipSourceAddresses)
        if forceDomains.isEmpty == false {
            map["force-domain"] = forceDomains
        }
        if skipDomains.isEmpty == false {
            map["skip-domain"] = skipDomains
        }
        if effectiveSkipDestinationAddresses.isEmpty == false {
            map["skip-dst-address"] = effectiveSkipDestinationAddresses
        }
        if skipSourceAddresses.isEmpty == false {
            map["skip-src-address"] = skipSourceAddresses
        }
        return map
    }

    private static let sparkleCompatibleSnifferSkipDestinationAddresses = [
        "91.105.192.0/23", "91.108.4.0/22", "91.108.8.0/21",
        "91.108.16.0/21", "91.108.56.0/22", "95.161.64.0/20",
        "149.154.160.0/20", "185.76.151.0/24", "2001:67c:4e8::/48",
        "2001:b28:f23c::/47", "2001:b28:f23f::/48", "2a0a:f280:203::/48"
    ]

    private func enforceAppManagedControlChannel(in map: inout YAMLMap, settings: AppSettings) {
        map["external-controller"] = "\(controllerBindHost(settings)):\(settings.controllerPort)"
        map.removeValue(forKey: "external-controller-unix")
        map.removeValue(forKey: "external-controller-pipe")

        let secret = settings.controllerSecret.trimmingCharacters(in: .whitespacesAndNewlines)
        if secret.isEmpty {
            map.removeValue(forKey: "secret")
        } else {
            map["secret"] = secret
        }
    }

    private func enforceNetworkRuntimePlan(in map: inout YAMLMap, settings: AppSettings) {
        let plan = NetworkRuntimePlan(settings: settings)
        guard plan.tunEnabled else { return }

        var tun = map["tun"] as? YAMLMap ?? [:]
        tun["enable"] = true
        if tun["stack"] == nil { tun["stack"] = "mixed" }
        if tun["auto-route"] == nil { tun["auto-route"] = true }
        if tun["auto-detect-interface"] == nil { tun["auto-detect-interface"] = true }
        tun["dns-hijack"] = plan.dnsHijackTargets
        map["tun"] = tun

        var dns = map["dns"] as? YAMLMap ?? dnsMap(settings, plan: plan)
        dns["enable"] = true
        if dns["enhanced-mode"] == nil { dns["enhanced-mode"] = settings.dnsEnhancedMode }
        map["dns"] = dns

        var profile = map["profile"] as? YAMLMap ?? [:]
        profile["store-fake-ip"] = plan.usesFakeIP
        map["profile"] = profile
    }

    private func portList(_ text: String, fallback: [Int]) -> [Any] {
        let values: [Any] = lineList(text).map { value in
            Int(value).map { $0 as Any } ?? value
        }
        return values.isEmpty ? fallback.map { $0 as Any } : values
    }

    private func dnsMap(_ settings: AppSettings, plan: NetworkRuntimePlan) -> YAMLMap {
        var map: YAMLMap = [
            "enable": true,
            "enhanced-mode": settings.dnsEnhancedMode,
            "nameserver": settings.dnsNameservers.isEmpty ? ["system"] : settings.dnsNameservers,
            "default-nameserver": ["tls://223.5.5.5"],
            "use-hosts": false,
            "use-system-hosts": false
        ]
        if plan.usesFakeIP {
            map["fake-ip-range"] = "198.18.0.1/16"
            map["fake-ip-filter"] = ["+.lan", "+.local", "time.*.com", "ntp.*.com", "+.push.apple.com"]
        }
        if settings.dnsFallbacks.isEmpty == false {
            map["fallback"] = settings.dnsFallbacks
        }
        return map
    }

    private func filterDisabledRules(in map: inout YAMLMap, disabledRules: Set<String>) {
        guard disabledRules.isEmpty == false,
              let rules = map["rules"] as? [Any]
        else { return }

        map["rules"] = rules.filter { item in
            guard let rule = item as? String else { return true }
            return disabledRules.contains(rule.trimmingCharacters(in: .whitespacesAndNewlines)) == false
        }
    }

    private func injectNodeProviders(_ nodeProviders: [NodeProvider], into map: inout YAMLMap) throws {
        let selected = nodeProviders.filter(\.enabled)
        guard selected.isEmpty == false else { return }

        var proxyProviders = map["proxy-providers"] as? YAMLMap ?? [:]
        for provider in selected {
            let name = provider.name.trimmingCharacters(in: .whitespacesAndNewlines)
            guard proxyProviders[name] == nil else {
                throw NSError(domain: "RuntimeConfigBuilder", code: 2, userInfo: [
                    NSLocalizedDescriptionKey: "独立节点提供商“\(name)”与 Profile 或覆写中的 proxy-provider 同名。请重命名其中一项后再启动。"
                ])
            }
            proxyProviders[name] = [
                "type": "http",
                "url": provider.url,
                "path": provider.path,
                "interval": provider.interval
            ]
        }
        map["proxy-providers"] = proxyProviders
    }

    private func lineList(_ text: String) -> [String] {
        text.components(separatedBy: CharacterSet(charactersIn: ",\n"))
            .map { $0.trimmingCharacters(in: .whitespacesAndNewlines) }
            .filter { !$0.isEmpty }
    }

    private func deepMerge(_ base: YAMLMap, _ overlay: YAMLMap) -> YAMLMap {
        var result = base
        for (key, value) in overlay {
            if let current = result[key] as? YAMLMap,
               let next = value as? YAMLMap {
                result[key] = deepMerge(current, next)
            } else {
                result[key] = value
            }
        }
        return result
    }

    private func yamlMapping(from content: String, label: String) throws -> YAMLMap {
        let trimmed = content.trimmingCharacters(in: .whitespacesAndNewlines)
        guard trimmed.isEmpty == false else { return [:] }
        let object = try Yams.load(yaml: content) ?? [:]
        guard let map = normalizeYAMLValue(object) as? YAMLMap else {
            throw NSError(domain: "RuntimeConfigBuilder", code: 1, userInfo: [
                NSLocalizedDescriptionKey: "\(label) 必须是 YAML 顶层映射。"
            ])
        }
        return map
    }

    private func normalizeYAMLValue(_ value: Any) -> Any {
        if let map = value as? YAMLMap {
            return map.reduce(into: YAMLMap()) { result, pair in
                result[pair.key] = normalizeYAMLValue(pair.value)
            }
        }
        if let map = value as? [AnyHashable: Any] {
            return map.reduce(into: YAMLMap()) { result, pair in
                result[String(describing: pair.key)] = normalizeYAMLValue(pair.value)
            }
        }
        if let array = value as? [Any] {
            return array.map { normalizeYAMLValue($0) }
        }
        return value
    }
}

struct NetworkRuntimePlan: Equatable {
    let tunEnabled: Bool
    let dnsEnabled: Bool
    let usesFakeIP: Bool
    let dnsHijackTargets: [String]
    let snifferEnabled: Bool

    init(settings: AppSettings) {
        tunEnabled = settings.tunEnabled
        dnsEnabled = settings.dnsEnabled || settings.tunEnabled
        usesFakeIP = dnsEnabled && settings.dnsEnhancedMode == "fake-ip"
        dnsHijackTargets = tunEnabled && dnsEnabled ? ["any:53"] : []
        snifferEnabled = settings.snifferEnabled
    }
}
